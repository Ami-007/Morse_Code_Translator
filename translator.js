export const translate = (message) => {
    if (message == "bonjour") {
        return "hello";
    } else if (message == "je m'appelle") {
        return "my name is";
    } else if (message == "oui") {
        return "yes";
    } else if (message == "non") {
        return "no";
    } else if (message == "au revoir") {
        return "goodbye";
    }
};
